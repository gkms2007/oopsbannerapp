public class oopsbannerappuc3 {
    public static void main(String[] args) {

        // Each line is built using String.join() instead of +
        System.out.println(String.join("", "  **  ", "  **  ", "  **  ", "  **  "));
        System.out.println(String.join("", " *     * ", " *     * ", " *     * ", " *       "));
        System.out.println(String.join("", " *     * ", " *     * ", " *     * ", " *       "));
        System.out.println(String.join("", " *     * ", " *     * ", "  **  ", "  **  "));
        System.out.println(String.join("", " *     * ", " *     * ", " *       ", "       * "));
        System.out.println(String.join("", " *     * ", " *     * ", " *       ", "       * "));
        System.out.println(String.join("", "  **  ", "  **  ", " *       ", "  ***  "));
    }
}